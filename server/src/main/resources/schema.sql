CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255) NOT NULL,
  email VARCHAR(512) NOT NULL,
  CONSTRAINT pk_user PRIMARY KEY (id),
  CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);
CREATE TABLE IF NOT EXISTS requests (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    created TIMESTAMP NOT NULL,
    description VARCHAR(500),
    owner_id BIGINT NOT NULL,
    CONSTRAINT pk_request PRIMARY KEY (id),
    CONSTRAINT fk_requests_to_users FOREIGN KEY(owner_id) REFERENCES users(id)
);
CREATE TABLE IF NOT EXISTS items (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255) NOT NULL,
  description VARCHAR(512) NOT NULL,
  owner_id BIGINT NOT NULL,
  available BOOL NOT NULL,
  request_id BIGINT,
  CONSTRAINT pk_item PRIMARY KEY (id),
  CONSTRAINT fk_items_to_users FOREIGN KEY(owner_id) REFERENCES users(id),
  CONSTRAINT fk_items_to_requests FOREIGN KEY(request_id) REFERENCES requests(id)
);
CREATE TABLE IF NOT EXISTS bookings (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  item_id BIGINT NOT NULL,
  booker_id BIGINT NOT NULL,
  start TIMESTAMP NOT NULL,
  "end" TIMESTAMP NOT NULL,
  status VARCHAR(10) NOT NULL CHECK (status IN ('APPROVED', 'WAITING', 'REJECTED')),
  CONSTRAINT pk_bookings PRIMARY KEY (id),
  CONSTRAINT fk_items_to_bookings FOREIGN KEY(item_id) REFERENCES items(id),
  CONSTRAINT fk_users_to_bookings FOREIGN KEY(booker_id) REFERENCES users(id)
);
CREATE TABLE IF NOT EXISTS comments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    item_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL,
    time TIMESTAMP NOT NULL,
    text VARCHAR(500),
    CONSTRAINT pk_comments PRIMARY KEY (id),
    CONSTRAINT fk_items_to_comments FOREIGN KEY(item_id) REFERENCES items(id),
    CONSTRAINT fk_users_to_comments FOREIGN KEY(user_id) REFERENCES users(id)
);
